package kr.co.seoulit.insa.newempsvc.newempinfomgmt.to;

public class ApplicantTO {
	private double personality_avg;
	private double interview_avg;
	private String code;
	private String name;
	private int age;
	private String dept;
	private String gender;
	private String last_school;
	private String tel;
	private String address;
	private String career;
	private String email;
	public double getPersonality_avg() {
		return personality_avg;
	}
	public void setPersonality_avg(double personality_avg) {
		this.personality_avg = personality_avg;
	}
	public double getInterview_avg() {
		return interview_avg;
	}
	public void setInterview_avg(double interview_avg) {
		this.interview_avg = interview_avg;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getDept() {
		return dept;
	}
	public void setDept(String dept) {
		this.dept = dept;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getLast_school() {
		return last_school;
	}
	public void setLast_school(String last_school) {
		this.last_school = last_school;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getCareer() {
		return career;
	}
	public void setCareer(String career) {
		this.career = career;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	
}
